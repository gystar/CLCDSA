#include<stdio.h>
int main() {
	int N, K;
	scanf("%d%d", &N, &K);
	int *D = (int *)malloc(sizeof(int)*K);
	int num[10] = { 0 };
	for (int i = 0; i < K; i++)
	{
		scanf("%d", &D[i]);
		num[D[i]] = 1;
	}
	for (int ans = N; ; ans++)
	{
		int NN = ans;
		while (1) {
			if (num[NN % 10] == 1) {
				break;
			}
			NN /= 10;
			if (NN == 0)
			{
				printf("%d\n", ans);
				return 0;
			}
		}
	}
	return 0;
} 